#include "ft_printf.h"

void	putstr(char *str, int *count)
{
	int	i;

	if (*count != -1)
	{
		if (!str)
			str = "(null)";
		i = 0;
		while (str[i] != '\0' && *count != -1)
			*count += write(1, &str[i++], 1);	
	}
}